class A 
{
	public static void main(String[] args) 
	{
		int i = 0;
		// i = i + 1;
		System.out.println(i++);
		System.out.println(i);
		System.out.println(i);
	}
}
//i = 0
//i -> 1
/*
Unary operators in Java
++[increment] [post-increment] [pre-increment]
--[decrement] [post-decrement] [pre-decrement]
*/
